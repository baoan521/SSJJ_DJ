using System;

namespace Veh
{
	public struct ImVec4
	{
		public ImVec4(float float_0, float float_1, float float_2, float float_3)
		{
			this.x = float_0;
			this.y = float_1;
			this.z = float_2;
			this.w = float_3;
		}

		public float x;

		public float y;

		public float z;

		public float w;
	}
}
